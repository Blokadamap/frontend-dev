/** Фото руководителя с запасным плейсхолдером (пока файл не загружен). */
function LeaderPhoto({ src, name }: { src: string; name: string }) {
  const [failed, setFailed] = useState(false);
  if (failed) {
    return (
      <span className="team-leader__photo team-leader__photo--empty" aria-hidden="true">
        <User size={34} strokeWidth={1.5} />
      </span>
    );
  }
  return (
    <span className="team-leader__photo">
      <img src={src} alt={`Фотография: ${name}`} onError={() => setFailed(true)} />
    </span>
  );
}

